export function getPresetById(id: string | undefined): CharacterPreset | null {
  if (!id) return null;
  return CHARACTER_PRESETS.find((p) => p.id === id) ?? null;
}

/**
 * Собирает итоговый системный промпт: базовая инструкция +
 * пресет характера + пользовательский промпт.
 */
export function buildSystemPrompt(
  base: string,
  presetId: string | undefined,
  customPrompt: string | undefined,
): string {
  const parts = [base];
  const preset = getPresetById(presetId);
  if (preset && preset.prompt) {
    parts.push(`\n[Характер ассистента: ${preset.name}]\n${preset.prompt}`);
  }
  if (customPrompt && customPrompt.trim()) {
    parts.push(
      `\n[Пользовательская инструкция]\n${customPrompt.trim()}`,
    );
  }
  return parts.join("\n");
}
