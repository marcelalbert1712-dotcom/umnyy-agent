function getSuggestions(text: string, cursorPos: number): string[] {
  const before = text.slice(0, cursorPos);
  const match = before.match(/([\wа-яё]+)$/i);
  if (!match) return [];
  const prefix = match[1].toLowerCase();
  if (prefix.length < 2) return [];
  return DICT.filter((w) => w.startsWith(prefix)).slice(0, 4);
}

function replaceWord(text: string, cursorPos: number, replacement: string): string {
  const before = text.slice(0, cursorPos);
  const after = text.slice(cursorPos);
  const match = before.match(/([\wа-яё]+)$/i);
  if (!match) return text;
  const wordStart = cursorPos - match[1].length;
  return text.slice(0, wordStart) + replacement + after;
}

