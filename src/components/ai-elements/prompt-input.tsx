export type PromptInputProps = {
  value: string;
  onChange: (value: string) => void;
  onSubmit: () => void;
  onStop: () => void;
  isStreaming: boolean;
  disabled?: boolean;
  placeholder?: string;
  className?: string;
  files: File[];
  onFilesChange: (files: File[]) => void;
  leftExtra?: React.ReactNode;
};

export function PromptInput({
  value,
  onChange,
  onSubmit,
  onStop,
  isStreaming,
  disabled,
  placeholder = "Сообщение…",
  className,
  files,
  onFilesChange,
  leftExtra,
}: PromptInputProps) {
  const ref = useRef<HTMLTextAreaElement>(null);
  const fileRef = useRef<HTMLInputElement>(null);
  const [cursorPos, setCursorPos] = useState(0);
  const [selectedSuggestion, setSelectedSuggestion] = useState(-1);
  const [isListening, setIsListening] = useState(false);
  const mediaRecorderRef = useRef<MediaRecorder | null>(null);
  const audioChunksRef = useRef<Blob[]>([]);
  const streamRef = useRef<MediaStream | null>(null);

  const isSupported = typeof navigator !== "undefined"
    && "mediaDevices" in navigator
    && "MediaRecorder" in window;

  const toggleListening = async () => {
    if (isListening) {
      mediaRecorderRef.current?.stop();
      streamRef.current?.getTracks().forEach(t => t.stop());
      streamRef.current = null;
      setIsListening(false);
      return;
    }

    try {
      const stream = await navigator.mediaDevices.getUserMedia({ audio: true });
      streamRef.current = stream;

      const mimeType = MediaRecorder.isTypeSupported("audio/webm;codecs=opus")
        ? "audio/webm;codecs=opus"
        : "audio/webm";

      const recorder = new MediaRecorder(stream, { mimeType });
      audioChunksRef.current = [];

      recorder.ondataavailable = (e) => {
        if (e.data.size > 0) audioChunksRef.current.push(e.data);
      };

      recorder.onstop = async () => {
        stream.getTracks().forEach(t => t.stop());
        streamRef.current = null;

        if (audioChunksRef.current.length === 0) return;

        const blob = new Blob(audioChunksRef.current, { type: mimeType });
        const buffer = await blob.arrayBuffer();
        const bytes = new Uint8Array(buffer);
        let binary = "";
        for (let i = 0; i < bytes.length; i++) {
          binary += String.fromCharCode(bytes[i]);
        }
        const base64 = btoa(binary);

        try {
          const res = await fetch("/api/transcribe", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ audio: base64, mimeType }),
          });
          const data = await res.json() as { text?: string; error?: string };
          if (data.text) {
            onChange(data.text);
          } else {
            console.error("Transcription error:", data.error ?? "empty response");
          }
        } catch (err) {
          console.error("Transcription fetch error:", err);
        }
      };

      mediaRecorderRef.current = recorder;
      recorder.start();
      setIsListening(true);
    } catch (err) {
      console.error("Microphone error:", err);
    }
  };

  const previews = files.map((f) => ({
    url: URL.createObjectURL(f),
    name: f.name,
    isImage: f.type.startsWith("image/"),
  }));

  const handleFilePick = (e: React.ChangeEvent<HTMLInputElement>) => {
    const picked = Array.from(e.target.files ?? []);
    if (picked.length === 0) return;
    onFilesChange([...files, ...picked]);
    e.target.value = "";
  };

  const removeFile = (idx: number) => {
    onFilesChange(files.filter((_, i) => i !== idx));
  };

  const suggestions = getSuggestions(value, cursorPos);

  // Слэш-команды
  const slashMatch = value.match(/^\/(\w*)$/);
  const showSlash = slashMatch !== null;
  const slashPrefix = slashMatch ? slashMatch[1].toLowerCase() : "";
  const filteredSlash = showSlash
    ? SLASH_COMMANDS.filter((s) => s.cmd.slice(1).startsWith(slashPrefix))
    : [];
  const [selectedSlash, setSelectedSlash] = useState(-1);

  useEffect(() => {
    const el = ref.current;
    if (!el) return;
    el.style.height = "auto";
    el.style.height = `${Math.min(el.scrollHeight, 220)}px`;
  }, [value]);

  const applySuggestion = (word: string) => {
    onChange(replaceWord(value, cursorPos, word));
    setSelectedSuggestion(-1);
    ref.current?.focus();
  };

  const handleKeyDown = (e: KeyboardEvent<HTMLTextAreaElement>) => {
    if (e.key === "Enter" && !e.shiftKey && selectedSuggestion === -1 && selectedSlash === -1) {
      e.preventDefault();
      if ((value.trim() || files.length > 0) && !isStreaming) onSubmit();
      return;
    }
    if (filteredSlash.length > 0) {
      if (e.key === "ArrowDown") {
        e.preventDefault();
        setSelectedSlash((prev) =>
          prev < filteredSlash.length - 1 ? prev + 1 : 0,
        );
        return;
      }
      if (e.key === "ArrowUp") {
        e.preventDefault();
        setSelectedSlash((prev) =>
          prev > 0 ? prev - 1 : filteredSlash.length - 1,
        );
        return;
      }
      if ((e.key === "Tab" || e.key === "Enter") && selectedSlash >= 0) {
        e.preventDefault();
        onChange(filteredSlash[selectedSlash].template);
        setSelectedSlash(-1);
        ref.current?.focus();
        return;
      }
    }
    if (suggestions.length > 0) {
      if (e.key === "ArrowDown") {
        e.preventDefault();
        setSelectedSuggestion((prev) =>
          prev < suggestions.length - 1 ? prev + 1 : 0,
        );
        return;
      }
      if (e.key === "ArrowUp") {
        e.preventDefault();
        setSelectedSuggestion((prev) =>
          prev > 0 ? prev - 1 : suggestions.length - 1,
        );
        return;
      }
      if ((e.key === "Tab" || e.key === "Enter") && selectedSuggestion >= 0) {
        e.preventDefault();
        applySuggestion(suggestions[selectedSuggestion]);
        return;
      }
    }
    setSelectedSuggestion(-1);
    setSelectedSlash(-1);
  };

  return (
    <div className={cn("relative", className)}>
      {suggestions.length > 0 && (
        <div className="absolute bottom-full left-0 right-0 mx-2 mb-1 flex flex-wrap gap-1">
          {suggestions.map((word, i) => (
            <button
              key={word}
              type="button"
              onMouseDown={(e) => {
                e.preventDefault();
                applySuggestion(word);
              }}
              className={cn(
                "rounded-lg border px-2.5 py-1 text-xs transition-colors",
                i === selectedSuggestion
                  ? "border-primary bg-primary/10 text-primary"
                  : "border-border bg-background text-muted-foreground hover:bg-accent",
              )}
            >
              {word}
            </button>
          ))}
        </div>
      )}
      {filteredSlash.length > 0 && (
        <div className="absolute bottom-full left-0 right-0 mx-2 mb-1 flex flex-wrap gap-1">
          {filteredSlash.map((s, i) => (
            <button
              key={s.cmd}
              type="button"
              onMouseDown={(e) => {
                e.preventDefault();
                onChange(s.template);
                setSelectedSlash(-1);
                ref.current?.focus();
              }}
              className={cn(
                "rounded-lg border px-2.5 py-1 text-xs transition-colors",
                i === selectedSlash
                  ? "border-primary bg-primary/10 text-primary"
                  : "border-border bg-background text-muted-foreground hover:bg-accent",
              )}
            >
              <span className="font-medium">{s.cmd}</span>
              <span className="ml-1 opacity-70">— {s.desc}</span>
            </button>
          ))}
        </div>
      )}
      {previews.length > 0 && (
        <div className="flex flex-wrap gap-2 px-1 pb-1">
          {previews.map((p, i) => (
            <div
              key={`${p.name}-${i}`}
              className="group relative flex items-center gap-1.5 rounded-lg border bg-card px-2 py-1.5 text-xs"
            >
              {p.isImage ? (
                <img
                  src={p.url}
                  alt={p.name}
                  className="size-8 rounded object-cover"
                />
              ) : (
                <span className="size-8 flex items-center justify-center rounded bg-muted text-[10px] font-medium text-muted-foreground">
                  {p.name.split(".").pop()?.toUpperCase()}
                </span>
              )}
              <span className="max-w-28 truncate">{p.name}</span>
              <button
                type="button"
                onClick={() => removeFile(i)}
                className="absolute -right-1.5 -top-1.5 flex size-4 items-center justify-center rounded-full bg-destructive text-destructive-foreground opacity-0 shadow transition-opacity group-hover:opacity-100"
              >
                <XIcon className="size-3" />
              </button>
            </div>
          ))}
        </div>
      )}
      <div
        className={cn(
          "relative flex items-end gap-2 rounded-2xl border border-border bg-background p-2 shadow-sm transition-shadow focus-within:shadow-md",
        )}
      >
        <input
          ref={fileRef}
          type="file"
          multiple
          accept="image/*,.pdf,.txt,.csv,.doc,.docx,.xls,.xlsx,.json,.xml"
          onChange={handleFilePick}
          className="hidden"
        />
        <button
          type="button"
          onClick={() => fileRef.current?.click()}
          disabled={disabled || isStreaming}
          className="mb-1 flex size-8 shrink-0 items-center justify-center rounded-lg text-muted-foreground transition-colors hover:bg-accent hover:text-foreground disabled:opacity-30"
          aria-label="Прикрепить файл"
          title="Прикрепить файл"
        >
          <PaperclipIcon className="size-4" />
        </button>
        {leftExtra}
        {isSupported && (
          <button
            type="button"
            onClick={toggleListening}
            disabled={disabled || isStreaming}
            className={cn(
              "mb-1 flex size-8 shrink-0 items-center justify-center rounded-lg transition-colors disabled:opacity-30",
              isListening
                ? "bg-destructive text-destructive-foreground animate-pulse"
                : "text-muted-foreground hover:bg-accent hover:text-foreground",
            )}
            aria-label={isListening ? "Остановить запись" : "Голосовой ввод"}
            title={isListening ? "Остановить запись" : "Голосовой ввод"}
          >
            {isListening ? <MicOffIcon className="size-4" /> : <MicIcon className="size-4" />}
          </button>
        )}
        <textarea
          ref={ref}
          value={value}
          onChange={(e) => {
            onChange(e.target.value);
            setCursorPos(e.target.selectionStart);
          }}
          onSelect={(e) =>
            setCursorPos((e.target as HTMLTextAreaElement).selectionStart)
          }
          onKeyDown={handleKeyDown}
          rows={1}
          placeholder={placeholder}
          disabled={disabled}
          lang="ru"
          spellCheck={true}
          autoCorrect="on"
          autoCapitalize="sentences"
          className="max-h-[220px] flex-1 resize-none bg-transparent px-3 py-2 text-sm outline-none placeholder:text-muted-foreground disabled:cursor-not-allowed"
        />
        {isStreaming ? (
          <Button
            type="button"
            size="icon"
            variant="default"
            onClick={onStop}
            aria-label="Остановить"
            title="Остановить"
            className="rounded-xl"
          >
            <SquareIcon className="size-4" />
          </Button>
        ) : (
          <Button
            type="button"
            size="icon"
            variant="default"
            onClick={() => (value.trim() || files.length > 0) && onSubmit()}
            disabled={(!value.trim() && files.length === 0) || disabled}
            aria-label="Отправить"
            title="Отправить"
            className="rounded-xl"
          >
            <ArrowUpIcon className="size-4" />
          </Button>
        )}
      </div>
    </div>
  );
}
